/**
 * Convert ISO 3166-1 alpha-3 country code to nationality name
 * @param {string} code - 3-letter country code from MRZ
 * @returns {string} Full nationality name or the code if not found
 */
export const convertCountryCodeToNationality = (code) => {
  if (!code || typeof code !== 'string') {
    return '';
  }

  const upperCode = code.toUpperCase().trim();
  return COUNTRY_CODE_TO_NATIONALITY[upperCode] || upperCode;
};

